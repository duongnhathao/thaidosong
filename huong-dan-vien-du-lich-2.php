                        <h3>Yêu cầu cần có của một hướng dẫn viên du lịch
                        </h3>
                        <p class="excert">
                            Để làm một hướng dẫn viên du lịch theo đúng quy định của pháp luật thì người hướng dẫn viên
                            cần phải có thẻ hướng dẫn viên du lịch quốc tế hoặc nội địa. Theo Luật du lịch nếu hướng dẫn
                            viên du lịch không có, không đeo hoặc cho mượn thẻ hướng dẫn trong lúc đang hành nghề thì sẽ
                            bị phạt.Và một điều quan trọng nữa là hướng dẫn viên phải có một nghiệp vụ du lịch vũng
                            vàng, hướng dẫn viên phải trang bị cho mình một nền tảng văn hóa, kiến thức, ngoại ngữ thật
                            vững và một bản lĩnh nghề nghiệp để sẵn sàng đối phó với bất kỳ tình huống phát sinh nào
                            trong suốt thời gian dẫn tour.
                        <p>
                            Hướng dẫn viên là các cá nhân làm việc trên tuyến du lịch cùng với cá nhân khác hoặc đoàn
                            khách theo 1 chương trình đã định sẵn, hướng dẫn viên có nhiệm vụ thuyết minh cho khách về
                            các điểm du lịch và tạo những ấn tượng tốt đẹp cho khách du lịch. </p>

                        <p>

                        <h3>Vai trò, nhiệm vụ của hướng dẫn viên du lịch
                        </h3>
                        <div class="feature-img mb-5">
                            <img class="img-fluid" src="img/blog/dulichlagi/frame-tree-cartoon-flower-heart.jpg"
                                 alt="Ảnh bìa">
                        </div>

                        <p>Hướng dẫn viên du lịch là người đại diện cho địa danh (địa điểm – vùng) giới thiệu với du khách về văn hóa, lịch sử… của nơi mà du khách đến tham quan. Người hướng dẫn viên đóng một vai trò cực kỳ quan trọng trong việc truyển tải văn hóa và tái hiện lại phần nào lịch sử thông qua bài thuyết minh của mình. Đặc biệt khi dấu vết thời gian của văn hóa và lịch sử không còn nhiều.Bài thuyết minh và cách diễn đạt của người hướng dẫn viên có ảnh hưởng trực tiếp tới cảm nhận của du khách – tác động lớn đến chất lượng chương trình tour. </p>
                        <p>Nói cách khác, vai trò của hướng dân viên là một trong những yếu tố quan trọng quyết định sự thành công hay không không thành công của một chương trình du lịch. Nó nâng tầm công ty du lịch, cũng như nâng tầm cho một địa danh hay đất nước với khách du lịch.Đạo đức nghề nghiệp của hướng dẫn viênNgười hướng dẫn viên du lịch tuyệt đối không được lợi dụng sự bỡ ngỡ của du khách để “vòi tiền” hay “ăn chặn”  tiền của du khách vì điều đó không chỉ làm ảnh hưởng đến uy tín, danh dự của bản thân người hướng dẫn viên mà còn làm ảnh hưởng chung đến hình ảnh công ty, nơi du khách đã đến tham quan và hình ảnh du lịch của đất nước.</p>
                        <p>Mỗi chúng ta hãy tự răn mình để không bị cám dỗ trước cái xấu, hãy luôn là người ngay thẳng thật thà trước mình và trước mọi người. Người hướng dẫn viên du lịch hãy trau dồi nghiệp vụ du lịch, tậm tâm trong nghề hướng dẫn viên, ham học hỏi… tạo nên sự uy tín và thương hiệu cho chính bản thân người hướng dẫn viên du lịch.</p>
                        <div class="feature-img mb-5">
                            <img class="img-fluid" src="img/blog/dulichlagi/4_69.jpg" alt="Ảnh bìa">
                        </div>
                        <p>Hướng dẫn viên du lịch đòi hỏi phải có một bài trình bày thật ấn tượng, chi tiết và đảm bảo
                            tính chính xác tuyệt đối mới có thể chinh phục được các du khách Việt. Muốn làm công việc
                            này bạn phải có thẻ nội địa của hướng dẫn viên du lịch.Hướng dẫn viên quốc tế đây là hướng
                            dẫn viên du lịch người Việt Nam nhưng sẽ sử dụng các ngôn ngữ khác nhau để phù hợp với du
                            khách. Bạn sẽ đón tiếp các đoàn khách từ nhiều quốc gia khác nhau đến du lịch tại Việt Nam.
                            Cũng như công việc của một hướng dẫn viên nội địa, việc làm này yêu cầu bạn phải có thẻ của
                            hướng dẫn viên quốc tế. Tuy nhiên, thẻ này sẽ không có giá trị khi bạn sang nước ngoài.Ngoài
                            ra còn có các loại hướng dẫn viên khác sau đây; On-site Guide (HDV tại điểm): là người hướng
                            dẫn du khách thực hiện chuyến tham quan trong vài giờ tại 1 điểm du lịch cụ thể, VD: hướng
                            dẫn khách thăm bảo tàng Hồ Chí Minh, cung đình Huế, Phố cổ Hội An,…City Guide (HDV thành
                            phố): là người hướng dẫn du khách thực hiện chuyến du lịch quanh thành phố, chủ yếu trên các
                            phương tiện công cộng như: xe buýt, taxi, xích lô,..với nhiệm cụ: giới thiệu, bình luận
                            những điểm du lịch nổi bật trong thành phố, ngoài tra HDV còn giải đáp các thắc mắc của du
                            khách trong lộ trình tham quan trên các phương tiện di chuyển.Step-on Guide (HDV không
                            chuyên): là các cộng tác viên được các tổ chức kinh doanh du lịch thuê theo hợp đồng để
                            hướng dẫn du khách. Họ có thể là: giáo viên ngoại ngữ, nhà báo, nhà khoa học,…có kiến thức
                            về các tuyến hoặc điểm du lịch mà du khách cần tìm hiểu. HDV dạng này đa phần cũng có khả
                            năng hướng dẫn du lịch và khả năng ứng xử linh hoạt với khách và thường được thê theo mùa du
                            lịch cao điểm hoặc làm tại những tuyến du lịch cố định nào đó.</p>
                        <p>HDV địa phương: là hướng dẫn viên tại những điểm du lịch hoặc thành phố cụ thể nào đó làm
                            nhiệm vụ hướng dẫn du khách ở 1 điểm du lịch đó chứ không theo khách trong suốt chuyến du
                            lịch. HDV địa phương ít nhiều gì cũng cần có những kiến thức nhất định về đối tượng tham gia
                            và nghiệp vụ, chuyên môn, không giống những người giới thiệu tại chỗ, vốn không phải là
                            hướng dẫn viên.</p>
